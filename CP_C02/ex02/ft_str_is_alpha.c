#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	a;

	a = 0;
	while ((str[a] >= 65 && str[a] <= 90) || (str[a] >= 97 && str[a] <= 122))
	{
		a++;
	}
	if (str[a] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
