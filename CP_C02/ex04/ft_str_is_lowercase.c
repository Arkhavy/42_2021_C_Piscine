#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	int	a;

	a = 0;
	while (str[a] >= 97 && str[a] <= 122)
	{
		a++;
	}
	if (str[a] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
