#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_display(char **tab)
{
	int	x;
	int	y;

	x = 0;
	while (x < 4)
	{
		y = 0;
		while (y < 4)
		{
			ft_putchar(tab[x][y]);
			if (y != 3)
				ft_putchar(' ');
			y++;
		}
		ft_putchar('\n');
		x++;
	}
}
