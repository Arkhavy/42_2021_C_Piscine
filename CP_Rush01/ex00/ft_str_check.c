int	ft_str_check(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if (i % 2 == 0 && !(str[i] > '0' && str[i] < '5'))
			return (0);
		else if (i % 2 == 1 && str[i] != ' ')
			return (0);
	}
	return (i);
}
