#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	a;

	a = 1;
	if (nb < 1)
		return (0);
	while ((a * a) < nb && (a <= 46340))
		a++;
	if ((a * a) == nb)
		return (a);
	return (0);
}
